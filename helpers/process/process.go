package process

// GetPortOwnerPID returns PID of a process which is an owning of local TCP port
func GetPortOwnerPID(localTCPPort int) (int, error) {
	return doGetPortOwnerPID(localTCPPort)
}

// GetBinaryPathByPID returns absolute path of process binary
func GetBinaryPathByPID(pid int) (string, error) {
	return doGetBinaryPathByPID(pid)
}
